public class main{
  public static void main(String[] args){
    char tc=65; //특정 문자에 저장된 숫자코드
    int ti=65;
    System.out.println(ti+" "+tc);
  }
}

public class main{
  public static void main(String[] args){
    char tc='b';
    int ti=(int) tc;
    System.out.println(ti+" "+tc);
  }
}


public class main{
  public static void main(String[] args){
    String t='Jeju'; //문자 4개+null 1개-> 5개 동시 선언
    char cc = t.charAt(0);
    int ci=(int) cc;
    System.out.println(cc+" "+ci);
  }
}
